/**
 * The `outputSpeech` object.
 */
class OutputSpeech {
  public type: string;
  public text?: string;
  public ssml?: string;

  /**
   * Creates an instance of OutputSpeech.
   *
   * @param {string} type
   * A string containing the type of output speech to render. Valid types are:
   * * "PlainText": Indicates that the output speech is defined as plain text.
   * * "SSML": Indicates that the output speech is text marked up with SSML.
   *
   * @param {string} [text]
   * A string containing the speech to render to the user. Use this when type is "PlainText".
   *
   * @param {string} [ssml]
   * A string containing text marked up with SSML to render to the user. Use this when type is  "SSML"
   *
   * @memberof OutputSpeech
   */
  constructor(type: string, text?: string, ssml?: string) {
    if (!type) throw new Error("Invalid output speech type.");
    this.type = type;
    if (type === "PlainText") {
      if (!text) throw new Error("Type is PlainText but no plain text is present.");
      this.text = text;
    } else if (type === "SSML") {
      if (!ssml) throw new Error("Type is SSML but no ssml is present.");
      this.ssml = ssml;
    }
  }
}

