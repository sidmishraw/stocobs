/**
 * The ticker object.
 * @class Ticker
 */
class Ticker {
  /**
   * The name of the company as listed in NASDAQ.
   * @type {string}
   * @memberof Ticker
   */
  public companyName: string;

  /**
   * The ticker symbol as listed in NASDAQ
   * @type {string}
   * @memberof Ticker
   */
  public ticker: string;

  /**
   * Creates an instance of Ticker.
   * @param {string} companyName The company name.
   * @param {string} ticker The ticker symbol.
   * @memberof Ticker
   */
  constructor(companyName: string, ticker: string) {
    this.companyName = companyName;
    this.ticker = ticker;
  }
}

/**
 * The TickerTrie will be used for fast ticker symbol lookup.
 * @class TickerTrie
 */
class TickerTrie {
  /**
   * The root of the ticker trie. All lookups start from here.
   * @private
   * @type {TickerNode}
   * @memberof TickerTrie
   */
  private root: TickerNode;

  /**
   * Creates an instance of TickerTrie.
   * @memberof TickerTrie
   */
  constructor() {
    this.root = new TickerNode();
  }

  /**
   * Inserts the value identified by the key in the trie for a fast lookup.
   * @param {string} key The key.
   * @param {string} value The value to be looked up.
   * @memberof TickerTrie
   */
  public put(key: string, value: string) {
    const filteredChars: Array<string> = this.getFilteredChars(key);
    ///////////////////// traversal /////////////////////
    let currNode: TickerNode = this.root;
    filteredChars.forEach((c: string) => {
      if (!currNode[c]) currNode[c] = new TickerNode();
      currNode = currNode[c];
    });
    ///////////////////// traversal /////////////////////
    // reached last node in the trie
    currNode.values.push(value);
    ////////////////////////////////////////////////////
  }

  /**
   * Fetches the values for the given lookup key.
   *
   * @param {string} key The lookup key.
   * @returns {Array<string>} The values for the given lookup key.
   * @memberof TickerTrie
   */
  public get(key: string): Array<string> {
    const filteredChars: Array<string> = this.getFilteredChars(key);
    ///////////////////// traversal /////////////////////
    let currNode: TickerNode = this.root;
    filteredChars.forEach((c: string) => {
      if (!currNode) return;
      else currNode = currNode[c];
    });
    ///////////////////// traversal /////////////////////
    // reached the last node
    if (!currNode) return [];
    else return currNode.values;
  }

  /**
   * Filters the characters of bad characters.
   * @private
   * @param {string} key The lookup key.
   * @returns {Array<string>} The list of valid characters.
   * @memberof TickerTrie
   */
  private getFilteredChars(key: string): Array<string> {
    return key.split("").filter((char: string) => {
      switch (char) {
        case "a":
        case "b":
        case "c":
        case "d":
        case "e":
        case "f":
        case "g":
        case "h":
        case "i":
        case "j":
        case "k":
        case "l":
        case "m":
        case "n":
        case "o":
        case "p":
        case "q":
        case "r":
        case "s":
        case "t":
        case "u":
        case "v":
        case "w":
        case "x":
        case "y":
        case "z":
        case " ":
        case ".":
          return true;
        default:
          return false;
      }
    });
  }
}

